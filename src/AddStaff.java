    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management_system","root","");
            
            Statement stmt=con.createStatement();
            String sql="Select * from users where UID='"+uidField.getText()+"'";
            ResultSet rs=stmt.executeQuery(sql);       
            if(rs.next()){
                PreparedStatement statement = con.prepareStatement("UPDATE users SET Username=?,Password=? WHERE UID='"+uidField.getText()+"' ");
                statement.setString(1,userField.getText());
                statement.setString(2,passwordField.getText());
                statement.executeUpdate();
                JOptionPane.showMessageDialog(null, "successfully updated!");                
            }else{
                JOptionPane.showMessageDialog(null, "invalid update!");
            }
       }catch(Exception e){}
    }//GEN-LAST:event_jButton4ActionPerformed

