    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management_system","root","");
            
            Statement stmt=con.createStatement();
            String sql="Select * from users where UID='"+jTextField2.getText()+"'";
            ResultSet rs=stmt.executeQuery(sql);
            
            if(rs.next()){
                PreparedStatement statement = con.prepareStatement("UPDATE users SET Username=?,Password=? WHERE UID='"+jTextField2.getText()+"' ");
                statement.setString(1,jTextField1.getText());
                statement.setString(2,jPasswordField1.getText());
                statement.executeUpdate();
                JOptionPane.showMessageDialog(null, "successfully updated!");                
            }else{
                JOptionPane.showMessageDialog(null, "invalid update!");
            }
            
           }catch(Exception e){
               JOptionPane.showMessageDialog(null, "ERROR!");
           }
    }//GEN-LAST:event_jButton2ActionPerformed

