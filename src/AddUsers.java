    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management_system","root","");
            PreparedStatement statement = con.prepareStatement("INSERT INTO  users(UID,Username,Password,Admin) VALUES(?,?,?,?)");
            statement.setString(1,jTextField2.getText());
            statement.setString(2,jTextField1.getText());
            statement.setString(3,jPasswordField1.getText());
            statement.setString(4,"user");
            
            Statement stmt=con.createStatement();
            String sql="Select * from users where UID='"+jTextField2.getText()+"'";
            ResultSet rs=stmt.executeQuery(sql);
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "this user already exists!");
            }else{
                statement.executeUpdate();
                JOptionPane.showMessageDialog(null, "Inserted succesfully!");
            }
                  
            
        } catch (Exception e) {
            System.out.println("Invalid Input!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

